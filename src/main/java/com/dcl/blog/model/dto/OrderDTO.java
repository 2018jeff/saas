package com.dcl.blog.model.dto;

public class OrderDTO {
	private String name;
	private String type;
	private float price;
	private float inprice;
	private double score;
	private String spell;
	private String model;
	private String goodRource;
	private long storeHouse;
	private String marks;
	private double num;
	private float countPrice;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public float getInprice() {
		return inprice;
	}
	public void setInprice(float inprice) {
		this.inprice = inprice;
	}
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}
	public String getSpell() {
		return spell;
	}
	public void setSpell(String spell) {
		this.spell = spell;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getGoodRource() {
		return goodRource;
	}
	public void setGoodRource(String goodRource) {
		this.goodRource = goodRource;
	}
	public long getStoreHouse() {
		return storeHouse;
	}
	public void setStoreHouse(long storeHouse) {
		this.storeHouse = storeHouse;
	}
	public String getMarks() {
		return marks;
	}
	public void setMarks(String marks) {
		this.marks = marks;
	}
	public double getNum() {
		return num;
	}
	public void setNum(double num) {
		this.num = num;
	}
	public float getCountPrice() {
		return countPrice;
	}
	public void setCountPrice(float countPrice) {
		this.countPrice = countPrice;
	}
	
}
